package codegen.main.business.model;

public class I18N {

   private String key;

   private String en;

   private String zh;

   private String tw;

   public String getKey() {
      return key;
   }

   public void setKey(String key) {
      this.key = key;
   }

   public String getEn() {
      return en;
   }

   public void setEn(String en) {
      this.en = en;
   }

   public String getZh() {
      return zh;
   }

   public void setZh(String zh) {
      this.zh = zh;
   }

   public String getTw() {
      return tw;
   }

   public void setTw(String tw) {
      this.tw = tw;
   }

  
}
